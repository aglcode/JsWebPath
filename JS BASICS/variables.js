// Variables: var, let, const

// var - old school
// let - mutable | you can still edit the value of your variable
// const - immutable | you cannot re-asign the value
// const is used for variables that should not be changed


// let x;
// x = 41; // initialize the value of x here


let x = 41; // initialize the value of x 
x = 'hi'; // with let i can change the value of x 

const y = 31; // for const i cannot change the value of y

console.log(x, y);
