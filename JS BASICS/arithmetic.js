// Arithmetic operators: +, -, *, /, **, %, ++, --

let x = 5;

console.log(x + 5); // add, = 10
console.log(x - 5); // subtract, = 0
console.log(x * 5); // multiply, = 25
console.log(x / 5); // divide,  = 1
console.log(x ** 5); // x to the power of 5, = 3125
console.log(x % 2);  // remainder, = 1


// ++ increment % -- decrement
console.log(x); // 5
x++; // increment the variable x, by 1
console.log(x); // 6
x--; // decrement the vairable x, by 1
console.log(x); // 5



